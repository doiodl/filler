#include "ft_printf.h"

int	retf(float x)
{
	return (((*(uint32_t*)(&x) >> 23) & (uint32_t)0xFF) - 127);
}

int	retd(double x)
{
	return (((*(uint64_t *)(&x) >> 52) & 0x7FF) - 1023);
}

int	retld(double x)
{
	return (((*(__uint128_t*)(&x) >> 64) & 0x7FFF) - 16383);
}
